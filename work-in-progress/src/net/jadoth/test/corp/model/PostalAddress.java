package net.jadoth.test.corp.model;


public final class PostalAddress
{
	////////////////////////////////////////////////////////////////////////////
	// instance fields //
	////////////////////

	final Street street;
	final String number;



	////////////////////////////////////////////////////////////////////////////
	// constructors //
	/////////////////

	public PostalAddress(final Street street, final String number)
	{
		super();
		this.street = street;
		this.number = number;
	}



	///////////////////////////////////////////////////////////////////////////
	// declared methods //
	/////////////////////

	public final Street street()
	{
		return this.street;
	}

	public final String number()
	{
		return this.number;
	}



	///////////////////////////////////////////////////////////////////////////
	// override methods //
	/////////////////////

	@Override
	public String toString()
	{
		return this.street().toString() + ' ' + this.number();
	}

}
