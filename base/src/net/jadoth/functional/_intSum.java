package net.jadoth.functional;

public final class _intSum implements _intProcedure
{
	///////////////////////////////////////////////////////////////////////////
	// instance fields //
	////////////////////

	private int result;



	///////////////////////////////////////////////////////////////////////////
	// constructors //
	/////////////////

	public _intSum()
	{
		super();
	}

	public _intSum(final int result)
	{
		super();
		this.result = result;
	}


	///////////////////////////////////////////////////////////////////////////
	// declared methods //
	/////////////////////

	public final int yield()
	{
		return this.result;
	}



	///////////////////////////////////////////////////////////////////////////
	// override methods //
	/////////////////////

	@Override
	public final void accept(final int value)
	{
		this.result += value;
	}

}
